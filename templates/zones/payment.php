<h3>4.4 Resiliation anticipee - consequences de la rupture</h3>
<p>
    4.4.1 Le présent accord pourra être résilié par le Prestataire avec effet immédiat sans indemnités dans les hypothèses suivantes:
    <ul>
        <li>
            défaillance du Client laquelle est définie comme l'inexécution de ses obligations contractuelles, à condition que l'inexécution reprochée se poursuive plus de 7 jours à compter de la mise en demeure de s'exécuter adressée par le Prestataire. En cas d'urgence  justifiée, la rupture sera à effet immédiat.<br/>
            La résiliation aura lieu sans préjudice de la faculté pour le Prestataire de demander le solde du Montant dut, le cas echeant, en accord avec le Calendrier.
        </li>
        <li>
            risque d'insolvabilité et/ou risque sérieux de défaut d'exécution de ses obligations par le Client.
        </li>
    </ul>
</p>
<p>
    4.4.2 En cas de rupture anticipée du Contrat pour quelque cause que ce soit, et si les Prestations n'ont pas encore été réceptionnées par le Client, le Prestataire notifiera au Client la date à laquelle il sera procédé à un constat contradictoire d'état des lieux et d'avancement des Prestations. Un décompte de résiliation sera alors effectué entre les Parties au vu des résultats obtenus par le Prestataire au jour de la rupture. En l'absence d'un représentant du Client à cette date, le constat sera réputé contradictoire.
</p>
<p>
    Il est entendu que, sauf rupture anticipée imputable au Client, le Prestataire s'engage à remettre au Client les travaux dans leur état d'avancement au jour de la rupture. Le Client règlera au prestataire les prestations d'ors et déjà réalisées.
</p>
<p>
    4.4.3 Parce que le Prestataire pourvoie un travail sur mesure pour le Client, le Client est responsable du paiement a l'attention du Prestataire pour tout le travail effectué pour le Client, peu importe si le Client ne parvient pas à utiliser le code. Dans le cas d'une résiliation, le Client aura acces au code, créé spécifiquement pour ce projet, qui aura déjà été écrit par le Prestataire.
</p>
<p>
    Si le Client annule le projet, il reste responsable des heures déjà travaillées par le Prestataire ainsi que toutes les dépenses engagées pour le projet. En plus de ces frais, un dédommagement de 10% du coût total estimé du projet sera payé par le Client.
</p>

<h3>4.5 Calendrier et Responsabilités du Client</h3>
<p>
    Le Client et le Prestataire doivent respecter le Calendrier décrivant les étapes du projet, à moins que les deux parties conviennent de modifications par écrit. La correspondance Email entre les parties constitue "par écrit" aux fins du présent contrat.
</p>
<p>
    Le Client comprend que le Projet créé par le Prestataire ne sea pas disponibles pour livraison jusqu'à ce que les paiements soint integralement reçus pour ce projet. Voir le Calendrier de paiement pour les dates, si elles sont fournies.
</p>
<p>
    Le Client est responsable du respect de tous les délais fixés par le Prestataire. Tout retard dans le calendrier du projet par le Client sont facturable par le Prestataire. Le Prestataire est chargé d'informer le Client de toutes les échéances et le montant facturable si le délai n'a pas été respecté.
</p>

