<h2>ARTICLE 4 : CONDITIONS FINANCIERES</h2>

<h3>4.1 Paiement</h3>
<p>
    Le Client accepte de payer au Prestataire le Montant en totalité ou, le cas échéant, selon le plan de paiement (ci-après <b>" Calendrier "</b>).
</p>
<p>
    Le Prestataire envoie des factures périodiques au client et le paiement est dû à réception de la facture. Le calendrier de ces factures périodiques dépend des dates repères prédéterminés pour chaque étape de projet (ci-après <b>" Étapes "</b>).
</p>
<p>
    Ces Étapes ont été déterminées d'un commun accord entre le Client et le Prestataire pour représenter les carrefours importants dans le processus de développement du Projet. Ils assurent une progression du Projet de manière satisfaisante pour les Parties.
</p>

<h3>4.2 Paiement tardif</h3>
<p>
    Le défaut de faire des paiements à temps, sans le consentement écrit préalable du Prestataire, se traduira par l'évaluation des frais de retard pour tout traitement supplémentaire qui sera nécessaire.
</p>
<p>
    Les frais de retard sont les suivantes:
    <ul>
        <li>
            Un frais de retard initial (5% du montant facturé) sera évalué si le paiement est de 15 jours de retard.
        </li>
        <li>
            Un deuxième frais de retard (10% du montant facturé) sera évalué si le compte est de 31 jours de retard.
        </li>
        <li>
            Pour ce contrat, la correspondance électronique sera considéré comme "consentement écrit" pour cet accord.
        </li>
    </ul>
</p>
<h3>4.3 Non Paiement</h3>
<p>
    Le défaut de paiement soumettra également le Client à une éventuelle action en justice pour que le Prestataire puisse recueillir les soldes impayés. Les frais juridiques engagés par le Prestataire peut devenir la responsabilité du Client.
</p>

